let container=document.getElementById("product")

function append(data){
    container.innerHTML=null;
    data.forEach((el,i)=>{
        
         
 

    let hiddenDiv=document.createElement("div");
    hiddenDiv.setAttribute("class","hiddenDiv")

    let cart=document.createElement("div");
    
     cart.innerText="SHOP NNNOW"
     let cartimg=document.createElement("img")
     cartimg.setAttribute=("class","add")
     cartimg.src="https://cdn-icons-png.flaticon.com/128/737/737035.png"
   
     cart.onclick=function(){
    
        addToCart(el)
     }



    let fav=document.createElement("div");
  
 

    fav.innerText="FAVORITE"
    let favItem=document.createElement("img")
    favItem.src="https://cdn-icons-png.flaticon.com/128/8184/8184221.png"
    favItem.setAttribute=("class","add")

     fav.onclick=function(){
        addToFav(el)
     }






    hiddenDiv.append(cartimg,cart,fav, favItem)












    let div=document.createElement("div");
    let img=document.createElement("img");
    img.setAttribute("class","productImage")
    img.src=el.img1;

  
    div.onmouseover=function hchange(){
        img.src=el.img2;
       
    }
    div.onmouseleave=function leave(){
        img.src=el.img1;
       
    }

    let titile= document.createElement("h3");
        titile.innerText=el.titile;
    
    let name=document.createElement("p");
    name.innerText=el.name;
    
    let price=document.createElement("h3");
    price.innerText=`Rs.`+el.price;

    
    div.append(img,hiddenDiv,titile,name,price,)
    container.append(div)
  
  })

}

